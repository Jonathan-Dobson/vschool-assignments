var color = "yellow"

switch(color){
    case "green":
        console.log("the color is green")
        break;
    case "blue":
        console.log("the color is blue")
        break;
    case "yellow":
        console.log("the color is yellow")
        break;
    default: 
        console.log("The color " + color +", was not accounted for")
}

